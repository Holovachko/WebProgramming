function month(n){
    switch(n){
        case 1:
        return "січень"
        break
        case 2:
        return "лютий"
        break
        case 3:
        return "березень"
        break
        case 4:
        return "квітень"
        break
        case 5:
        return "травень"
        break
        case 6:
        return "червень"
        break
        case 7:
        return "липень"
        break
        case 8:
        return "серпень"
        break
        case 9:
        return "вересень"
        break
        case 10:
        return "жовтень"
        break
        case 11:
        return "листопад"
        break
        case 12:
        return "грудень"
        break
    }

}